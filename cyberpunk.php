    <h2>CyberPunk</h2>
    <br>
    <p>Cyberpunk é um subgênero da ficção científica que surgiu na década de 1980, tendo como principal característica a ambientação em um futuro distópico e tecnológico, onde a tecnologia avançada e a sociedade se fundem de forma caótica.

Nesse universo, é comum encontrar grandes corporações controlando a vida das pessoas, governos autoritários e uma ampla desigualdade social. A tecnologia é onipresente, mas muitas vezes é utilizada para opressão, vigilância e controle.

O termo "cyberpunk" foi cunhado pelo escritor Bruce Sterling em 1983, para descrever o estilo de escrita de um grupo de autores de ficção científica que exploravam temas como tecnologia, cibercultura, distopias e subculturas urbanas. Entre os principais autores do movimento estão William Gibson, Neal Stephenson e Bruce Sterling.

Nos anos 80 e 90, o movimento cyberpunk teve grande influência na cultura pop, inspirando filmes como "Blade Runner" e "Matrix", além de jogos de RPG e videogames como "Shadowrun" e "Deus Ex".

Atualmente, o cyberpunk continua a inspirar a cultura pop, principalmente em jogos de videogame como "Cyberpunk 2077", lançado em 2020, que se passa em um futuro distópico em que a tecnologia modificou completamente a sociedade, com implantes cibernéticos e megacorporações controlando tudo.

No entanto, assim como em seu universo ficcional, o movimento cyberpunk também tem sido criticado por alguns por sua visão pessimista e fatalista do futuro, além de sua falta de representatividade em relação a minorias sociais. Ainda assim, o cyberpunk continua a ser uma fonte de inspiração e fascínio para muitos, explorando as consequências de uma sociedade cada vez mais dependente da tecnologia.</p>
